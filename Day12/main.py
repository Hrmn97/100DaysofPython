#String Slicing & Operations on String



#Length of a String -> We can find the length of a string using len() function.

fruit = "Mango"
len1 = len(fruit)
print("Mango is a", len1, "letter word.")

name=len("Harman")
print(name)

#SLICING

names="harman, Singh"

print(names[0:5])
print(names[:5])
print(names[2:6])
print(names[0:-3])

apple="Mango"
print(apple[-3:-1])  #This gets equivalent to 2:4


nm="Harry"
print(nm[-4:-2])
